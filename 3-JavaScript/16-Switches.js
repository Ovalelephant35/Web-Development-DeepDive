//switch statement = instead of using mutiple if else statement we can use switch cases
//for a switch case match against a clause more efficent else if
let gr = "x";
switch(gr){
    case "A":
        console.log("You Got A");
        break;
    case "B":
        console.log("You Got B");
        break;
    case "C":
        console.log("You Got C");
        break;
    case "D":
        console.log("You Got D");
        break;
    case "E":
        console.log("You Got E");
        break;
    default:
        console.log("Invalid");
}